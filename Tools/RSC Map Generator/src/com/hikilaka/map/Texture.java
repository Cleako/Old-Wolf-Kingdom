	public static int decodeColor(int i) {
		if (i == 0xbc614e) {
			return 0;
		}
		if (i >= 0) {
			return textureColors[i];
		}
		if (i < 0) {
			i = -(i + 1);
			int j = i >> 10 & 0x1f;
			int k = i >> 5 & 0x1f;
			int l = i & 0x1f;
			return (j << 19) + (k << 11) + (l << 3);
		} else {
			return 0;
		}
	}

