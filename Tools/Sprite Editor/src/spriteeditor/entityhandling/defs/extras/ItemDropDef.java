package spriteeditor.entityhandling.defs.extras;

/**
 * Defines an item drop definition
 * (Useless for the sprite editor,
 *  simply kept to keep NPCDef happy).
 */
public class ItemDropDef 
{
	/**
	 * This item drop's id
	 */
	public int id;
	/**
	 * The amount of this item that is dropped
	 */
	public int amount;
	/**
	 * The weight of possibility that this item will be dropped
	 */
	public int weight;
}